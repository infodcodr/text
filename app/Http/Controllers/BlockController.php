    {
        try{
            $block = New Block();
            $block = $block->with('user')->where('blocked_by',auth()->user()->id)->get();
            $data['data'] = $block;
            $data['message'] = 'block';
            return  $this->apiResponse($data,200);
        }catch(\Exception $e)
        {
            $data['message'] = $e->getMessage();
